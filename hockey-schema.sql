DROP SCHEMA IF EXISTS hockey_schema;

CREATE SCHEMA hockey_schema;
USE hockey_schema;

CREATE TABLE team (
    team_code VARCHAR(3) NOT NULL,
    team_name VARCHAR(100),
    PRIMARY KEY (team_code)
);

CREATE TABLE season (
    season_year INT,
    season_label VARCHAR(20),
    num_games INT,
    PRIMARY KEY (season_year)
);

CREATE TABLE player (
    player_id INT,
    player_name VARCHAR(100),
    position VARCHAR(2),
    shoots VARCHAR(1),
    birth_date DATE,
    weight INT,
    height VARCHAR(10),
    nationality VARCHAR(3),
    jersey_number INT,
    current_team VARCHAR(3),
    PRIMARY KEY (player_id),
    FOREIGN KEY (current_team) REFERENCES team(team_code)
);

CREATE TABLE game (
    game_id INT,
    season_year INT,
    date DATE,
    home_team VARCHAR(3),
    away_team VARCHAR(3),
    home_score INT,
    away_score INT,
    home_win BOOLEAN AS (home_score > away_score) STORED,
    is_playoffs BOOLEAN,
    PRIMARY KEY (game_id),
    FOREIGN KEY (season_year) REFERENCES season(season_year),
    FOREIGN KEY (home_team) REFERENCES team(team_code),
    FOREIGN KEY (away_team) REFERENCES team(team_code)
);

CREATE TABLE player_game_stats (
    player_id INT,
    game_id INT,
    situation VARCHAR(10),
    icetime INT,
    shifts INT,
    game_score FLOAT,
    goals INT,
    primary_assists INT,
    secondary_assists INT,
    points INT AS (goals + primary_assists + secondary_assists) STORED,
    shots_on_goal INT,
    missed_shots INT,
    blocked_shot_attempts INT,
    shot_attempts INT AS (shots_on_goal + missed_shots + blocked_shot_attempts) STORED,
    x_goals FLOAT,
    rebounds INT,
    rebound_goals INT,
    rebound_x_goals FLOAT,
    x_goals_from_x_rebounds FLOAT,
    x_goals_from_actual_rebounds FLOAT,
    high_danger_shots INT,
    medium_danger_shots INT,
    low_danger_shots INT,
    high_danger_x_goals FLOAT,
    medium_danger_x_goals FLOAT,
    low_danger_x_goals FLOAT,
    high_danger_goals INT,
    medium_danger_goals INT,
    low_danger_goals INT,
    flurry_adjusted_x_goals FLOAT,
    score_venue_adjusted_x_goals FLOAT,
    hits INT,
    takeaways INT,
    giveaways INT,
    d_zone_giveaways INT,
    face_offs_won INT,
    penalties INT,
    penalty_minutes INT,
    o_zone_shift_starts INT,
    d_zone_shift_starts INT,
    neutral_zone_shift_starts INT,
    fly_shift_starts INT,
    shots_blocked_by_player INT,
    on_ice_f_goals INT,
    on_ice_f_x_goals FLOAT,
    on_ice_f_shot_attempts INT,
    on_ice_f_high_danger_shots INT,
    on_ice_f_high_danger_goals INT,
    on_ice_f_high_danger_x_goals FLOAT,
    on_ice_a_goals INT,
    on_ice_a_x_goals FLOAT,
    on_ice_a_shot_attempts INT,
    on_ice_a_high_danger_shots INT,
    on_ice_a_high_danger_goals INT,
    on_ice_a_high_danger_x_goals FLOAT,
    x_goals_for_after_shifts FLOAT,
    x_goals_against_after_shifts FLOAT,
    PRIMARY KEY (player_id, game_id, situation),
    CONSTRAINT chk_player_situation CHECK (situation IN ('all','5on5','5on4','4on5','other')),
    FOREIGN KEY (player_id) REFERENCES player(player_id),
    FOREIGN KEY (game_id) REFERENCES game(game_id)
);

