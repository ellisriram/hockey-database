CREATE TABLE shot (
    shot_id INT AUTO_INCREMENT,
    game_id INT NOT NULL,
    team_code VARCHAR(3) NOT NULL,
    shooter_player_id INT,
    goalie_id INT,
    event VARCHAR(10),
    shot_type VARCHAR(20),
    shot_on_empty_net BOOLEAN,
    shot_rebound BOOLEAN,
    shot_rush BOOLEAN,
    shot_generated_rebound BOOLEAN,
    shot_goalie_froze BOOLEAN,
    shot_play_stopped BOOLEAN,
    shot_play_continued_in_zone BOOLEAN,
    shot_play_continued_outside_zone BOOLEAN,
    x_cord_adjusted FLOAT,
    y_cord_adjusted FLOAT,
    arena_adjusted_x_cord FLOAT,
    arena_adjusted_y_cord FLOAT,
    shot_angle_adjusted FLOAT,
    arena_adjusted_shot_distance FLOAT,
    x_goal FLOAT,
    x_rebound FLOAT,
    x_freeze FLOAT,
    shot_angle_plus_rebound FLOAT,
    shot_angle_rebound_royal_road BOOLEAN,
    period INT,
    time INT,
    home_team_goals INT,
    away_team_goals INT,
    home_empty_net BOOLEAN,
    away_empty_net BOOLEAN,
    home_skaters_on_ice INT,
    away_skaters_on_ice INT,
    last_event_category VARCHAR(30),
    last_event_team VARCHAR(3),
    last_event_x_cord_adjusted FLOAT,
    last_event_y_cord_adjusted FLOAT,
    distance_from_last_event FLOAT,
    speed_from_last_event FLOAT,
    time_since_last_event FLOAT,
    time_since_faceoff FLOAT,
    player_position_that_did_event VARCHAR(2),
    shooter_time_on_ice FLOAT,
    shooter_time_on_ice_since_faceoff FLOAT,
    time_difference_since_change FLOAT,
    average_rest_difference FLOAT,
    PRIMARY KEY (shot_id),
    FOREIGN KEY (game_id) REFERENCES game(game_id),
    FOREIGN KEY (team_code) REFERENCES team(team_code),
    FOREIGN KEY (shooter_player_id) REFERENCES player(player_id),
    FOREIGN KEY (goalie_id) REFERENCES player(player_id)
);